    private void btnAceptarPedirLibroFisicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarPedirLibroFisicoActionPerformed
        if(controlador.getGuardarLibrosFisicos().buscarLibroBoolean(ftxtISBNPedirLibroFisico.getText()) != true){
            JOptionPane.showMessageDialog(null,"El libro no esta registrado", "Libro no creado",
                    JOptionPane.INFORMATION_MESSAGE);
        }else{
            if(controlador.getGuardarLibrosFisicos().
                buscarLibro(ftxtISBNPedirLibroFisico.getText()).getCantidadEjemplares() <= 0){
                JOptionPane.showMessageDialog(null,"No hay ejemplares disponibles", "Disponibilidad",
                        JOptionPane.INFORMATION_MESSAGE);
                framePedirLibroFisico.setVisible(false);
                frameMenuClienteExistente.setVisible(true);
            }else{
                int cantidadEjemplares =  controlador.getGuardarLibrosFisicos().
                buscarLibro(ftxtISBNPedirLibroFisico.getText()).getCantidadEjemplares();
        
                int cantidadPrestados = controlador.getGuardarLibrosFisicos().
                buscarLibro(ftxtISBNPedirLibroFisico.getText()).getCantidadPrestados();
                
                controlador.getGuardarClientes().buscarPersona(txtNumIdCliente.getText()).
                    agregarLibroPrestado(controlador.getGuardarLibrosFisicos().
                            buscarLibro(ftxtISBNPedirLibroFisico.getText()));
                
                controlador.getGuardarClientes().buscarPersona(txtNumIdCliente.getText()).
                    agregarLibroPrestadoFisico(controlador.getGuardarLibrosFisicos().
                            buscarLibro(ftxtISBNPedirLibroFisico.getText()));

                controlador.getGuardarLibrosFisicos().buscarLibro(ftxtISBNPedirLibroFisico.getText()).
                    setCantidadPrestados(cantidadPrestados + 1);

                controlador.getGuardarLibrosFisicos().buscarLibro(ftxtISBNPedirLibroFisico.getText()).
                        setCantidadEjemplares(cantidadEjemplares - 1);

                controlador.getGuardarTodosLibros().buscarLibro(ftxtISBNPedirLibroFisico.getText()).
                        setCantidadPrestados(cantidadPrestados + 1);

                controlador.getGuardarTodosLibros().buscarLibro(ftxtISBNPedirLibroFisico.getText()).
                        setCantidadEjemplares(cantidadEjemplares - 1);

                JOptionPane.showMessageDialog(null, "El libro fisico se ha prestado exitosamente",
                        "Libro fisico prestado", JOptionPane.INFORMATION_MESSAGE);

                framePedirLibroFisico.setVisible(false);
                frameMenuClienteExistente.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnAceptarPedirLibroFisicoActionPerformed

