                eliminarLibroPrestado(ftxtISBNDevolverLibroFisico.getText());
            
            controlador.getGuardarLibrosFisicos().buscarLibro(ftxtISBNDevolverLibroFisico.
                    getText()).setCantidadEjemplares(cantidadEjemplares + 1);
            
