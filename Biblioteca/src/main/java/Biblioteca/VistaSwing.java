    private void btnAceptarLibroElectronicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarLibroElectronicoActionPerformed
        //Verifica que el libro no este creado, si no lo esta lo crea
        String fechaTitulo = controlador.pasarDateAString(dateFechaTituloLibroElectronico.getDate());
        String fechaIngreso = controlador.pasarDateAString(dateFechaIngresoLibroElectronico.getDate());
        
        if(controlador.getGuardarLibrosElectronicos().buscarLibroBoolean(ftxtISBNLibroElectronico.getText()) != true){
            
            controlador.getGuardarLibrosElectronicos().addInicioListaLibro(new LibroElectronico(
                    txtTituloLibroElectronico.getText(), txtAutorLibroElectronico.getText(), 
                    txtEditorialLibroElectronico.getText(), Integer.parseInt(ftxtNumPaginasLibroElectronico.getText()), 
                    fechaIngreso, fechaTitulo, ftxtISBNLibroElectronico.getText(), 
                    txtGeneroLibroElectronico.getText(), txaReferenciasLibroElectronico.getText()));
            
            controlador.getGuardarTodosLibros().addInicioListaLibro(new LibroElectronico(
                    txtTituloLibroElectronico.getText(), txtAutorLibroElectronico.getText(), 
                    txtEditorialLibroElectronico.getText(), Integer.parseInt(ftxtNumPaginasLibroElectronico.getText()), 
                    fechaIngreso, fechaTitulo, ftxtISBNLibroElectronico.getText(), 
                    txtGeneroLibroElectronico.getText(), txaReferenciasLibroElectronico.getText()));
            
            JOptionPane.showMessageDialog(null, "El eBook se ha registrado exitosamente", "eBook Registrado",
                JOptionPane.INFORMATION_MESSAGE);
            frameAñadirLibroElectronico.setVisible(false);
            frameMenuEmpleadoExistente.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "No se puede registrar el libro porque ya existe",
                    "eBook ya registrado",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnAceptarLibroElectronicoActionPerformed

