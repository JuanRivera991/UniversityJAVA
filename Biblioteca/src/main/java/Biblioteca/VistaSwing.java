    private void btnAceptarEliminarLibroFisicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarEliminarLibroFisicoActionPerformed
        for(int i = 0; i< controlador.getGuardarClientes().getTamañoPersona(); i++){
            try {
                for(int j =0; j<controlador.getGuardarClientes().getPersona(i).getListaLibrosFisicos().
                        getTamañoLibro(); j++){
                    try {
                        if(controlador.getGuardarClientes().getPersona(i).getListaLibrosFisicos().
                                buscarLibroBoolean(ftxtISBNEliminarLibroFisico.getText()) == true){
                            
                            controlador.getGuardarClientes().getPersona(i).
                                    eliminarLibroPrestadoFisicos(ftxtISBNEliminarLibroFisico.getText());
                            
                            controlador.getGuardarClientes().getPersona(i).
                                    eliminarLibroPrestado(ftxtISBNEliminarLibroFisico.getText());
                        }
                    } catch (Exception ex) {
                        Logger.getLogger(VistaSwing.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } catch (Exception ex) {
                Logger.getLogger(VistaSwing.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String respuesta = controlador.getGuardarLibrosFisicos().removerLibro(ftxtISBNEliminarLibroFisico.getText());
        controlador.getGuardarTodosLibros().removerLibro(ftxtISBNEliminarLibroFisico.getText());
        JOptionPane.showMessageDialog(null, respuesta, "Eliminar Libro Fisico",
                JOptionPane.INFORMATION_MESSAGE);
        frameEliminarLibroFisico.setVisible(false);
        frameMenuEmpleadoExistente.setVisible(true);
    }//GEN-LAST:event_btnAceptarEliminarLibroFisicoActionPerformed

