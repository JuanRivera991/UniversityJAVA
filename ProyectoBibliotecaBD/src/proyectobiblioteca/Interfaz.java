    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        clientes = conex.cargarClientes(clientes);
        int edad = Integer.parseInt(txtEdadRegis.getText());
        char[] contraseñaChar = txtContraRegis.getPassword();
        char[] repetirContraChar = txtRepetirContraRegis.getPassword();
        String contraseña = conex.contraseñaString(contraseñaChar);
        String repetirContra = conex.contraseñaString(repetirContraChar);
        boolean estaCreado = false;
        
        for(Cliente cliente:clientes){
            if(cliente.getNumId().equals(txtNumIdRegis.getText())){
                estaCreado = true;
            }
        }
        
        if(estaCreado == true){
            JOptionPane.showMessageDialog(null, "Ya hay un cliente con esa identificación");
        }else{
            if(contraseña.equals(repetirContra)){
                
                conex.registroClientes(txtNumIdRegis.getText(), txtNombreRegis.getText(), txtApellidosRegis.getText(), cmbTipoIdRegis.getSelectedItem().toString(), edad, 
                    contraseña, txtDireccRegis.getText());

                clientes.add(new Cliente(txtNumIdRegis.getText(), txtNombreRegis.getText(), txtApellidosRegis.getText(), cmbTipoIdRegis.getSelectedItem().toString(), edad, 
                    contraseña, txtDireccRegis.getText()));
                
                FrameRegistro.setVisible(false);
                this.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "La contraseña no coincide");
            }
        }
        clientes = conex.cargarClientes(clientes);
   
    }//GEN-LAST:event_btnRegistrarseActionPerformed

