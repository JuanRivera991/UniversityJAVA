    private void btnIngresarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarLoginActionPerformed
        clientes = conex.cargarClientes(clientes);
        String idLogin = txtIdLogin.getText();
        char[] contraseñaChar = txtContraseñaLogin.getPassword();
        String contraseña =  conex.contraseñaString(contraseñaChar) ;
        int band = 0;
        
        try {
            conex.stmt = conex.con.createStatement();
            conex.rs = conex.stmt.executeQuery("SELECT * FROM clientes");
            conex.rs.next();
            
            if(conex.rs.isFirst()== false){
                JOptionPane.showMessageDialog(null, "No hay ningun cliente registrado");
            }else{
                
                while(conex.rs.isAfterLast() == false){
                    if(idLogin.equals(conex.rs.getString("idCliente")) && contraseña.equals(conex.rs.getString("contraseña"))){
                        JOptionPane.showMessageDialog(null, "Bienvenido " + conex.rs.getString("nombre") + " " + conex.rs.getString("apellidos"));
                        
                        for(Cliente cliente:clientes){
                            if(cliente.getNumId().equals(idLogin) && cliente.getContraseña().equals(contraseña)){
                                clienteLogin = cliente;
                            }
                        }
                        
                        lblNombreClieMenu.setText(clienteLogin.getNombre() + " " + clienteLogin.getApellidos());
                        this.setVisible(false);
                        FrameMenu.setVisible(true);
                        band = 1;
                    }
                    conex.rs.next();
                }
                if(conex.rs.isAfterLast() == true && band == 0){
                    JOptionPane.showMessageDialog(null, "Ese cliente no esta registrado");
                }
            }
            
            conex.rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        
    }//GEN-LAST:event_btnIngresarLoginActionPerformed

