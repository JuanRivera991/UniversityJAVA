    private void btnEditarEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarEditActionPerformed
        clientes = conex.cargarClientes(clientes);
        int edad = Integer.parseInt(txtEdadEdit.getText());
        char[] contraseñaChar = txtContraEdit.getPassword();
        char[] repetirContraChar = txtRepetirContraEdit.getPassword();
        String contraseña = conex.contraseñaString(contraseñaChar);
        String repetirContra = conex.contraseñaString(repetirContraChar);
        boolean estaCreado = false;
        int i = 0;
        int index = 0;
        
        for(Cliente cliente:clientes){
            if(cliente.getNumId().equals(lblNumeroIdEdit.getText())){
                estaCreado = true;
                index = i;
            }
            i++;
        }
        
        if(estaCreado == true){
            System.out.println("Cliente encontrado");
            if(contraseña.equals(repetirContra)){
                
                conex.editarClientes(lblNumeroIdEdit.getText(), txtNombreEdit.getText(), txtApellidosEdit.getText(), cmbTipoIdEdit.getSelectedItem().toString(), edad, 
                    contraseña, txtDireccEdit.getText());
                
                clientes.get(index).setNombre(txtNombreEdit.getText());
                clientes.get(index).setApellidos(txtApellidosEdit.getText());
                clientes.get(index).setTipoId(cmbTipoIdEdit.getSelectedItem().toString());
                clientes.get(index).setEdad(edad);
                clientes.get(index).setContraseña(contraseña);
                clientes.get(index).setDireccion(txtDireccEdit.getText());

                FrameEditar.setVisible(false);
                FrameMenu.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "La contraseña no coincide");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Cliente no encontrado");
        }
        
        clientes = conex.cargarClientes(clientes);
        lblNombreClieMenu.setText(txtNombreEdit.getText()+ " " + txtApellidosEdit.getText());
    }//GEN-LAST:event_btnEditarEditActionPerformed

